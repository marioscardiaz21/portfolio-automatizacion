		FROM #DESCUENTO_V2 A
		LEFT JOIN (	SELECT TRX_NUMBER, NOMBRE_CARTERA, SUM(CAST(AMOUNT_APPLIED_SOLES AS MONEY)) AS AMOUNT_APPLIED_SOLES FROM (	
						SELECT * FROM (
					SELECT A.*, PAGO_TOTAL  FROM (SELECT TRX_NUMBER, NOMBRE_CARTERA, 
														CAST(A.FECHA_PAGO_SISTEMA AS date) AS FECHA_PAGO_SISTEMA,
														SUM(CAST(AMOUNT_APPLIED_SOLES AS MONEY)) AS AMOUNT_APPLIED_SOLES 
														FROM CLARO.PAGOS A WITH(NOLOCK)
														WHERE A.FECHA_CARGA = CAST(GETDATE() AS DATE) 
														AND A.NOMBRE_CARTERA NOT LIKE '%PJ%'
														AND A.NOMBRE_CARTERA NOT LIKE '%P_M%'
														AND A.NOMBRE_CARTERA NOT LIKE '%WHATSAPP%'
														AND A.NOMBRE_CARTERA NOT LIKE '%P_FIJA%'
														AND A.NOMBRE_CARTERA NOT LIKE '%P_CVE%'
														
														GROUP BY TRX_NUMBER, NOMBRE_CARTERA, CAST(A.FECHA_PAGO_SISTEMA AS date)) A
					LEFT JOIN (SELECT RECIBO, CARTERA, PAGO_TOTAL, FECHA_PAGO  FROM #DESCUENTO_V2
								WHERE FECHA_PAGO IS NOT NULL
								AND CAST(PAGO_TOTAL AS MONEY) >0) RET ON RET.RECIBO = A.TRX_NUMBER
																		AND RET.CARTERA = A.NOMBRE_CARTERA
																		AND RET.PAGO_TOTAL = A.AMOUNT_APPLIED_SOLES
																		AND CAST(RET.FECHA_PAGO AS date) = CAST(A.FECHA_PAGO_SISTEMA AS date)

							) AS PAGOS_CLARO
							WHERE PAGO_TOTAL IS NULL
							) AS PAGO_UNIDO
							GROUP BY TRX_NUMBER, NOMBRE_CARTERA
							) PAGO ON PAGO.TRX_NUMBER =A.RECIBO
									AND PAGO.NOMBRE_CARTERA =A.CARTERA
		
		
		) AS DESCUENTO_V2
		) AS DESCUENTO_V2
