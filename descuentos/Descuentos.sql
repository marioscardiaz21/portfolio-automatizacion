				AND OFR.FECHA_PAGO IS NULL
				AND CAST(OFR.FECHA_FIN AS date) > CAST(GETDATE() AS date)
				AND CAST(OFR.FECHA_GESTION_DESDE_EL_INICIO_DE_CARTERA AS date) = CAST(GETDATE() AS date)
				AND OFR.FECHA_COMPROMISO_DESDE_EL_INICIO_DE_CARTERA IS NOT NULL
				AND CON.ACCION IS NULL
				AND YAH.FECHA IS NULL
				AND AJUSTE.TRX_NUMBER IS NULL 
				AND OTROS.TRX_NUMBER IS NULL
				AND CONCRETADO.RECIBO IS NULL
				AND CONCRETADO.RECIBO IS NULL
				AND PAGO.TRX_NUMBER IS NULL


