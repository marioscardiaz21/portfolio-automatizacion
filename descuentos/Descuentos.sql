		SELECT *,
		CAST(DESCUENTO AS float) - CAST(SALDOS_SOLES_FINAL AS float) AS AJUSTE_OFRECIDO_MENOS_SALDO_SOLES
		
		FROM (
		SELECT A.*, 
		CASE WHEN CAST(PAGO.AMOUNT_APPLIED_SOLES AS money) = CAST(PAGO_TOTAL AS money) OR CAST(PAGO.AMOUNT_APPLIED_SOLES AS money) IS NULL
				  THEN SALDO_SOLES ELSE CAST(SALDO_SOLES AS MONEY) - CAST(PAGO.AMOUNT_APPLIED_SOLES AS money) END AS SALDOS_SOLES_FINAL,
