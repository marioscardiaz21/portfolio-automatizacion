														AND A.NOMBRE_CARTERA NOT LIKE '%PJ%'
														AND A.NOMBRE_CARTERA NOT LIKE '%P_M%'
														AND A.NOMBRE_CARTERA NOT LIKE '%WHATSAPP%'
														AND A.NOMBRE_CARTERA NOT LIKE '%P_FIJA%'
														AND A.NOMBRE_CARTERA NOT LIKE '%P_CVE%'
														
														GROUP BY TRX_NUMBER, NOMBRE_CARTERA, CAST(A.FECHA_PAGO_SISTEMA AS date)) A
					LEFT JOIN (SELECT RECIBO, CARTERA, PAGO_TOTAL, FECHA_PAGO  FROM #DESCUENTO_V2 WITH(NOLOCK)
								WHERE FECHA_PAGO IS NOT NULL
								AND CAST(PAGO_TOTAL AS MONEY) >0) RET ON RET.RECIBO = A.TRX_NUMBER
																		AND RET.CARTERA = A.NOMBRE_CARTERA
																		AND RET.PAGO_TOTAL = A.AMOUNT_APPLIED_SOLES
																		AND CAST(RET.FECHA_PAGO AS date) = CAST(A.FECHA_PAGO_SISTEMA AS date)

							) AS PAGOS_CLARO
							WHERE PAGO_TOTAL IS NULL
							) AS PAGO_UNIDO
							GROUP BY TRX_NUMBER, NOMBRE_CARTERA
							) PAGO ON PAGO.TRX_NUMBER =OFR.RECIBO
									AND PAGO.NOMBRE_CARTERA =OFR.CARTERA

